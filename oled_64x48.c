static bool oled_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    return i2c_write_bytes(g_addr, buf, 2);
}

static bool oled_data(const uint8_t* data, uint32_t len) {
    uint8_t chunk[17];
    uint32_t i = 0;

    chunk[0] = 0x40;

    while (i < len) {
        uint32_t n = len - i;
        if (n > 16) n = 16;
        memcpy(&chunk[1], &data[i], n);
        if (!i2c_write_bytes(g_addr, chunk, n + 1)) {
            return false;
        }
        i += n;
    }

    return true;
}

static bool oled_cmd_list(const uint8_t* cmds, uint32_t len) {
    for (uint32_t i = 0; i < len; i++) {
        if (!oled_cmd(cmds[i])) {
            return false;
        }
    }
    return true;
}

bool oled_begin(uint8_t addr7) {
    static const uint8_t init_seq[] = {
        0xAE,
        0xD5, 0x80,
        0xA8, 0x2F,
        0xD3, 0x00,
        0x40,
        0x8D, 0x14,
        0x20, 0x00,
        0xA1,
        0xC8,
        0xDA, 0x12,
        0x81, 0x8F,
        0xD9, 0xF1,
        0xDB, 0x40,
        0xA4,
        0xA6,
        0x2E,
        0xAF
    };

    g_addr = addr7;
    memset(g_fb, 0, sizeof(g_fb));

    return oled_cmd_list(init_seq, sizeof(init_seq));
}

void oled_clear(void) {
    memset(g_fb, 0, sizeof(g_fb));
}

static void oled_draw_char(uint8_t x, uint8_t page, char c) {
    if (page >= OLED_PAGES || x > (OLED_W - 6)) {
        return;
    }

    if (c >= 'a' && c <= 'z') {
        c = (char)(c - 'a' + 'A');
    }

    if (c < ' ' || c > 'Z') {
        c = ' ';
    }

    const uint8_t* glyph = font5x7[(int)(c - ' ')];
    uint32_t base = page * OLED_W + x;

    for (int i = 0; i < 5; i++) {
        g_fb[base + i] = glyph[i];
    }
    g_fb[base + 5] = 0x00;
}

void oled_draw_string(uint8_t x, uint8_t page, const char* s) {
    while (*s && x <= (OLED_W - 6)) {
        oled_draw_char(x, page, *s++);
        x += 6;
    }
}

bool oled_display(void) {
    if (!oled_cmd(0x21)) return false;
    if (!oled_cmd(0x00)) return false;
    if (!oled_cmd(0x3F)) return false;
    if (!oled_cmd(0x22)) return false;
    if (!oled_cmd(0x00)) return false;
    if (!oled_cmd(0x05)) return false;

    return oled_data(g_fb, sizeof(g_fb));
}

uint8_t oled_get_addr(void) {
    return g_addr;
}